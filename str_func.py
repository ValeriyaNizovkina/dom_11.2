def upper(text):
    """
    Эта функция, которая принимает на вход строку и возвращает ее со всеми заглавными буквами.
    :return:
    """
    return text.upper()

def title(text):
    """
    функци которая принимает на вход строку, которая делает заглавными первые буквы каждого слова в строке.
    :param text:
    :return:
    """
    return text.title()

